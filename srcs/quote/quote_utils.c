#include "minishell.h"

int	cnt_c(char *line, char c)
{
	int	i;
	int	cnt;

	cnt = 0;
	i = 0;
	while (line[i])
	{
		if (line[i] == c)
			++cnt;
		++i;
	}
	return (cnt);
}
